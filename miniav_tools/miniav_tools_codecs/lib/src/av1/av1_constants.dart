/// AV1 spec constants — OBU types, sequence profile/level enums, default
/// values used throughout the Phase 0+ encoder.
///
/// References:
///   AV1 Bitstream & Decoding Process Specification v1.0.0 (2019-01-08)
///   §6.2.1 Trailing bits, §6.7.x OBU semantics, §6.4 Sequence header
library;

/// OBU type values (4-bit field). Spec §6.2.1.
class ObuType {
  static const int sequenceHeader = 1;
  static const int temporalDelimiter = 2;
  static const int frameHeader = 3;
  static const int tileGroup = 4;
  static const int metadata = 5;
  static const int frame = 6; // frame header + tile group, combined
  static const int redundantFrameHeader = 7;
  static const int tileList = 8;
  static const int padding = 15;
}

/// `seq_profile` values. We only support Main.
class SeqProfile {
  static const int main = 0; // 8/10-bit 4:0:0, 4:2:0
  static const int high = 1; // 8/10-bit 4:4:4
  static const int professional = 2; // 8/10/12-bit any subsampling
}

/// `seq_level_idx_0` encoded as `X.Y` → `(X-2)*4 + Y`.
/// Spec Annex A. We default to Level 4.0 (good up to 1920×1080 @ 30).
class SeqLevel {
  static const int level_2_0 = 0;
  static const int level_3_0 = 4;
  static const int level_4_0 = 8;
  static const int level_5_0 = 12;
  static const int level_6_0 = 16;
}

/// Frame-type values. Spec §6.8.2.
class FrameType {
  static const int key = 0;
  static const int inter = 1;
  static const int intraOnly = 2;
  static const int switchFrame = 3;
}

/// `color_primaries` (CICP). 1 = BT.709.
class ColorPrimaries {
  static const int bt709 = 1;
  static const int unspecified = 2;
  static const int bt601 = 6;
}

/// `transfer_characteristics` (CICP). 1 = BT.709, 13 = sRGB.
class TransferCharacteristics {
  static const int bt709 = 1;
  static const int unspecified = 2;
  static const int srgb = 13;
}

