/// Partial AVFormatContext layout — FFmpeg 8.x.
/// Only the prefix up to `pb` (the AVIOContext pointer) is mapped, which is
/// enough to wire an output file we opened ourselves via `avio_open`.
final class AVFormatContextPrefix extends Struct {
  external Pointer<Void> avClass; // const AVClass*
  external Pointer<Void> iformat; // const AVInputFormat*
  external Pointer<Void> oformat; // const AVOutputFormat*
  external Pointer<Void> privData;
  external Pointer<AVIOContext> pb;
}

/// `AVCodecParameters` partial layout — FFmpeg 8.x (libavcodec 62).
/// Only the prefix we care about for muxing video streams. The struct is
/// allocated by `avcodec_parameters_alloc()` so we never compute its size.
final class AVCodecParameters extends Struct {
  @Int32() // enum AVMediaType
  external int codecType;
  @Int32() // enum AVCodecID
  external int codecId;
  @Uint32()
  external int codecTag;
  external Pointer<Uint8> extradata;
  @Int32()
  external int extradataSize;
  external Pointer<Void> codedSideData;
  @Int32()
  external int nbCodedSideData;
  @Int32()
  external int format;
  @Int64()
  external int bitRate;
  @Int32()
  external int bitsPerCodedSample;
  @Int32()
  external int bitsPerRawSample;
  @Int32()
  external int profile;
  @Int32()
  external int level;
  @Int32()
  external int width;
  @Int32()
  external int height;
  // AVRational sample_aspect_ratio
  @Int32()
  external int sarNum;
  @Int32()
  external int sarDen;
  // AVRational framerate
  @Int32()
  external int frNum;
  @Int32()
  external int frDen;
}

/// `AVStream` partial prefix — FFmpeg 8.x (libavformat 62).
/// Allocated by `avformat_new_stream()`. We only read/write the first few
/// fields (index, codecpar pointer, time_base).
final class AVStream extends Struct {
  external Pointer<Void> avClass; // AVClass*
  @Int32()
  external int index;
  @Int32()
  external int id;
