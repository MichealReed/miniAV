/// Minimal hand-rolled FFI bindings for libavutil + libavcodec (+ a few
/// libavformat helpers).
///
/// **ABI pin**: this binding targets FFmpeg 8.x (libavutil 60, libavcodec 62,
/// libavformat 62) as shipped by the BtbN auto-downloader. `AVFrame` and
/// `AVPacket` struct layouts mirror those headers. If the loader picks up an
/// older shared library, only function symbols are guaranteed compatible —
/// the partial struct layouts here may misread pts/dts/etc.
///
/// We intentionally avoid mirroring `AVCodecContext`. All context configuration
/// goes through `av_opt_set_*` (string/int/q variants), which is ABI stable
/// across major FFmpeg versions.
// ignore_for_file: library_private_types_in_public_api
