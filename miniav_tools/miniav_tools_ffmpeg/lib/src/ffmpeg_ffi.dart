  @Int32()
  external int linesize3;
  @Int32()
  external int linesize4;
  @Int32()
  external int linesize5;
  @Int32()
  external int linesize6;
  @Int32()
  external int linesize7;

  external Pointer<Pointer<Uint8>> extendedData;

  @Int32()
  external int width;
  @Int32()
  external int height;
  @Int32()
