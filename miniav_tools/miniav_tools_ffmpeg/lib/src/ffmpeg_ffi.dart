  /// Returns the cached instance, or null if FFmpeg has not been loaded.
  ///
  /// Call [loader.tryLoadFFmpeg] or [loader.ensureFFmpegLoaded] first.
  static Ffmpeg? instance() {
    if (_instance != null) return _instance;
    final avutil = loader.avutil;
    final avcodec = loader.avcodec;
    if (avutil == null || avcodec == null) return null;
    return _instance = Ffmpeg._(avutil, avcodec, loader.avformat);
  }

