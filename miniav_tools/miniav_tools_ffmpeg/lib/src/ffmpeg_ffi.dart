  /// Wrap an AVERROR code into a Dart exception string.
  String strError(int err) {
    const bufLen = 256;
    final buf = calloc<Uint8>(bufLen).cast<Utf8>();
    try {
      avStrError(err, buf, bufLen);
      return buf.toDartString();
    } finally {
      calloc.free(buf);
    }
  }

